weather = "sunny"
case(weather)
when 'sunny'
puts "It's SUNNY!!!!  Go get that vitamin D"
when 'rainy'
puts "Don't forget those rain boots today, or you will be WETTTTTTT!!!"
when 'snowy'
puts "I love snow!  take your hat and gloves today"
when 'cloudy'
puts "it's cloudy, try not to be too depressed"
else 
puts "the weatherman is always wrong - be prepared"
end
